df.dtypes

"""4. Cek Duplikasi & Missing Value"""

# Jumlah data duplikat
df.duplicated().sum()

# Visualisasi missing value
plt.figure(figsize=(10,5))
sns.heatmap(df.isnull(), cbar=False, cmap='viridis')
plt.title('Missing Value Heatmap')
plt.show()

"""5. Analisis Univariate (Satu Variabel)"""

# Kolom numerik
num_cols = df.select_dtypes(include=np.number).columns
df[num_cols].hist(figsize=(12,8), bins=20, color='skyblue')
plt.suptitle('Distribusi Kolom Numerik')
plt.show()

# Kolom kategorikal
cat_cols = df.select_dtypes(exclude=np.number).columns
for col in cat_cols:
    plt.figure(figsize=(6,4))
    sns.countplot(data=df, x=col, palette='viridis')
    plt.title(f'Countplot {col}')
    plt.xticks(rotation=45)
    plt.show()

"""6. Analisis Bivariate (Dua Variabel)"""

# Korelasi antar variabel numerik
plt.figure(figsize=(10,6))
sns.heatmap(df.corr(numeric_only=True), annot=True, cmap='coolwarm')
plt.title('Heatmap Korelasi')
plt.show()

# Scatter plot (contoh)
sns.scatterplot(data=df, x='colom_x', y='colom_y', hue='colom_kategori')
plt.title('Hubungan antar variabel')
plt.show()

# Boxplot buat liat outlier
sns.boxplot(data=df, x='colom_kategori', y='colom_numerik')
plt.title('Boxplot per kategori')
plt.show()

"""7. Analisis Outlier"""

# Gunakan boxplot semua kolom numerik
for col in num_cols:
    plt.figure(figsize=(6,4))
    sns.boxplot(x=df[col], color='lightgreen')
    plt.title(f'Outlier pada {col}')
    plt.show()

